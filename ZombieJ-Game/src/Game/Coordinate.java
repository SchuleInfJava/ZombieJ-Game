package Game;

public class Coordinate {

	private double x;
	private double y;

	// construktor
	public Coordinate(double x, double y) {
		this.x = x;
		this.y = y;
	}

	// getter / setter
	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}

}
